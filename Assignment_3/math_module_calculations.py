
## Module 
import math

## Input a number 
num = float(input("Enter a number: "))

##  Square root of the number 
sqr = math.sqrt(num)
print(f"Square root: {sqr}")
## Natural logarithm (log base e) of the number

nat_log = math.log(num)
print(f"Logarithm: {nat_log}")

## Sine of the number (in radians)

sine_n = math.sin(num)
print(f"Sine: {sine_n}")
